public class DataValAndText {

    private String val;
    private String text;

    public DataValAndText(String val, String text) {
        this.val = val;
        this.text = text;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
